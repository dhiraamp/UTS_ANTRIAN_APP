    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) { 
        // TODO add your handling code here:
        try {
            // Cek apakah ada baris yang dipilih di tabel
            int selectedRow = TabelAntrian.getSelectedRow();
            if (selectedRow == -1) {
                // Tampilkan pesan jika tidak ada baris yang terpilih
                jOptionPane1.showMessageDialog(this, "Silakan pilih data yang ingin dihapus.");
                return; // Keluar dari metode jika tidak ada baris yang dipilih
            }

            // Ambil nomor antrian yang terpilih
            String nomorAntrian = TabelAntrian.getValueAt(selectedRow, 0).toString();

            // Koneksi ke database dan eksekusi query
            Connection conn = KoneksiDB.getConnection();
            String sql = "DELETE FROM antrian WHERE nomor_antrian = ?";
            var ps = conn.prepareStatement(sql);

            ps.setString(1, nomorAntrian);
            ps.execute();

            // Tampilkan pesan jika berhasil menghapus data
            jOptionPane1.showMessageDialog(this, "Data berhasil dihapus!");

            // Perbarui tampilan tabel
            DaftarAntrian daftarAntrian = new DaftarAntrian();
            daftarAntrian.setVisible(true);
        } catch (SQLException e) {
            // Tampilkan pesan kesalahan jika terjadi SQLException
            jOptionPane1.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        }
    }


