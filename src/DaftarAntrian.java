    private void tampilkanDataAntrian() {
        String sql = "SELECT nomor_antrian, nama, jenis_kelamin, alamat, kondisi FROM antrian";

        try (
            Connection conn = KoneksiDB.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql)
        ) {
            DefaultTableModel model = new DefaultTableModel();
            TabelAntrian.setAutoCreateRowSorter(true);
            model.addColumn("Nomor Antrian");
            model.addColumn("Nama");
            model.addColumn("Jenis Kelamin");
            model.addColumn("Alamat");
            model.addColumn("Kondisi");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("nomor_antrian"),
                    rs.getString("nama"),
                    rs.getString("jenis_kelamin"),
                    rs.getString("alamat"),
                    rs.getString("kondisi")
                });
            }

            TabelAntrian.setModel(model); // Ganti dengan JTable Anda
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Menampilkan Data: " + e.getMessage());
        }
    }


